//Every expression in JS can be evaluated down to a single, specific
//value result. For example:
var a = 3 * 6;
var b = a;
b;

//each of the three lines is a statement containing expressions.

//var a = 3 * 6 and var b = a are called “declaration statements”
//because they each declare a variable (and optionally assign a value to it

//The a = 3 * 6 and b = a assignments (minus the vars) 
//are called assignment expressions.

//3 * 6 is an expression a on the second line is also an expression, 
//as is b on the third line.

//The third line contains just the expression b, but it’s also a statement
//all by itself. As such, this is generally referred to as 
//an “expression statement.”